#Fundamental Data Types
1.
int
print(2 + 4) # 6
print(2 - 4) # -2
print(2 * 4) # 8
print(4 / 2) # 2
print(2 ** 3) # 8
print(5 // 4) # 1
print(6 % 4) # 2
print(type(2 + 4)) # <class 'int'>
print(type(2 - 4)) # <class 'int'>
print(type(2 * 4)) # <class 'int'>
print(type(4 / 2)) # <class 'int'>

2.
float
print(2 / 4) # 0.5
print(9.9 + 1.1) # 11.0
print(10.56 * 15.82) # 167.0592
print(type(10.56)) # <class 'float'>
print(type(9.9 + 1.1)) # <class 'float'>
print(type(10.56 * 15.82)) # <class 'float'>
print(type(2 / 4)) # <class 'float'>