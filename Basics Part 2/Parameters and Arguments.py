# Parameters
def say_hello(name, emoji):
    print(f'Helllooooo {name} {emoji}')
# Arguments
say_hello(name='Rituraj', emoji=':)') # Helllooooo Rituraj :)
say_hello(name='Brad', emoji=':)') # Helllooooo Brad :)
say_hello(name='Andy', emoji=':)') # Helllooooo Andy :)